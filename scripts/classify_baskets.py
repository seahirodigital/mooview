def get_basket_name(code, name):
    code_clean = code.split('/')[0].strip()
    
    # 1. 直接マッピングを確認
    if code_clean in BASKET_MAP:
        return BASKET_MAP[code_clean]
    
    # 2. フォールバックによる範囲分類
    return fallback_classify(code_clean)

def main():
    if not os.path.exists(TARGET_DIR):
        print(f"Error: Directory not found - {TARGET_DIR}")
        return

    csv_files = [f for f in os.listdir(TARGET_DIR) if f.endswith('.csv')]
    print(f"Found {len(csv_files)} CSV files to process.")

    for filename in csv_files:
        filepath = os.path.join(TARGET_DIR, filename)
        print(f"\nProcessing {filename}...")
        
        # CSVの読み込み
        rows = []
        with open(filepath, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            header = next(reader)
            
            # ヘッダーチェックと構築
            # C列（インデックス2）にバスケットを挿入する
            if "バスケット" in header:
                basket_idx = header.index("バスケット")
            else:
                header.append("バスケット")
                basket_idx = len(header) - 1
                
            for row in reader:
                if not row:
                    continue
                code = row[0]
                name = row[1] if len(row) > 1 else ""
                
                # バスケット判定
                basket = get_basket_name(code, name)
                
                # 行の長さを調整してC列に値をセット
                while len(row) <= basket_idx:
                    row.append("")
                row[basket_idx] = basket
                rows.append(row)
                
                print(f"  {code} - {name} => {basket}")

        # CSVの書き出し (上書き)
        with open(filepath, 'w', encoding='utf-8', newline='') as f:
            writer = csv.writer(f)
            writer.writerow(header)
            writer.writerows(rows)
            
        print(f"Successfully processed and overwrote {filename}.")

if __name__ == "__main__":
    main()
